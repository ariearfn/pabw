      <h2>About TØP</h2>
      <hr>
      <div class="clearfix"></div>
      <p>Little things that you need to know about |-/</p>
    </div>
    <div class="row">
      <div class="col-md-6"> <img src="../Work/assets/img/about.jpg" class="img-responsive"> </div>
      <div class="col-md-6">
        <div class="about-text">
          <h4>The History</h4>
          <p>Twenty One Pilots were formed in 2009 by high-school friends Tyler Joseph, Nick Thomas, and Chris Salih. The pop trio built an enormous regional following across Ohio and the Midwest, spurred on by constant energetic touring, shrewd use of interactive social media, and connecting with fans on a grassroots level. Twenty One Pilots self-released their eponymous debut album of rap-infused high-tech pop in December 2009 before Thomas and Salih left the band in 2011; Tyler Joseph carried on with new member Josh Dun.</p>
          <h4>The Debut</h4>
          <p>Later that year, second album Regional at Best surfaced, the first with the new duo lineup. The pair also gained popularity through publicly releasing a series of videos made by their friend Mark Eshelman. In 2012, they signed to Atlantic subsidiary Fueled by Ramen, releasing the Three Songs EP in the summer of that year and working with Adele producer Greg Wells on Vessel, their debut album for the label. Vessel saw release in January of 2013, and the band was scheduled to support it with an international tour deep into the year.</p>
          <h4>The Billboard</h4>
          <p>For the recording of their third album, Twenty One Pilots opted to work with various producers on an almost song-by-song basis. Working with Ricky Reed, Mike Crossey, Tim Anderson, and others, this process took them to studios throughout Los Angeles and London. The album, the Billboard 200-topping Blurryface, arrived in 2015. The album's "Stressed Out" single reached number two on the Billboard Hot 100 in February 2016, with "Ride" following at number six that summer. "Heathens," their contribution to the soundtrack for the DC Comics film Suicide Squad, not only helped notch another Billboard 200 chart-topper for the duo, but the single's rise to number four on the Hot 100 made Twenty One Pilots the first alternative band to have two concurrent Top Ten singles. At the end of the year, the duo collaborated with tourmates MUTEMATH for the TOPxMM EP, which featured reimaginings of "Heathens" and four Blurryface tracks. </p>
          <p>source : https://itunes.apple.com/ca/artist/twenty-one-pilots/349736311</p>
