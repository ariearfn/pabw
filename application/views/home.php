      <div class="col-md-3 col-sm-6 service"> <i class="fa fa-desktop"></i>
        <h4>What is the release date of the next album?</h4>
        <p>Will twenty one pilots release an album in 2017? Probably not. No exact release details are known but, as their touring schedule will take them through to the summer, it is safe to say that a 2018 release looks most likely for album number 5.</p>
      </div>
      <div class="col-md-3 col-sm-6 service"> <i class="fa fa-gears"></i>
        <h4>What is the title of Twenty One Pilots' new fifth album?</h4>
        <p>No details yet although some fans do have a couple of theories. Josh Dun popped over to Cleveland to pick up an award at the APMA's recently and made a little reference that has got a lot of fans talking during his speech: "Tyler wishes he could be here, but he's actually severing ties with dema". According to Wikipedia, the word 'Dema' means "tower of silence". Could this be in reference to the closing eye images we've seen on their social accounts? Is it a hint at the future? Or is it just an elaborate metaphor for working on new music?</p>
      </div>
      <div class="col-md-3 col-sm-6 service"> <i class="fa fa-rocket"></i>
        <h4>What will Twenty One Pilots new album sound like?</h4>
        <p>In an interview with Alt Press last year, Tyler revealed some of the initial ideas he's been playing with in the studio while out on the road. "I was working on something last night that I'm really excited about...Right now I think it would be a little less up-tempo than the last record. I would want to focus a little more on the lyrical content.</p>
      </div>
      <div class="col-md-3 col-sm-6 service"> <i class="fa fa-line-chart"></i>
        <h4>What will the next Twenty One Pilots album be about?</h4>
        <p>In the same interview, Tyler pulled out a similarity between his new ideas and the band's self-titled record. "There are parts of the self-titled record that, production-wise and vocally...There are so many things wrong with that record, or at least perceived as wrong. But the authenticity, lyrics, delivery, and the fearlessness of the songwriting, I'd like to try to take[TØP] back to that. It's really hard to write a slower song. I want to try to write some slower songs again".</p>
      </div>
